class ImageTile
  attr_reader :id

  def initialize(input = '')
    lines = input.split("\n")
    @id = find_id(lines.shift)
    @image0 = lines.map{ |l| l.split("") }
    @image90 = lines.transpose.map(&:reverse)
    @image180 = @image90.transpose.map(&:reverse)
    @image270 = @image180.transpose.map(&:reverse)
  end

  def find_id(id_line = '')
    id_line.split(" ")[1].delete(":").to_i
  end
end

